package com.dsep.entity.dsepmeta;

public class PublicitySet {
	private String id;
	private String unitId;
	private String discId;
	private String viewUnitId;
	private String viewDiscId;
	private String userType;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUnitId() {
		return unitId;
	}
	public void setUnitId(String unitId) {
		this.unitId = unitId;
	}
	public String getDiscId() {
		return discId;
	}
	public void setDiscId(String discId) {
		this.discId = discId;
	}
	public String getViewUnitId() {
		return viewUnitId;
	}
	public void setViewUnitId(String viewUnitId) {
		this.viewUnitId = viewUnitId;
	}
	public String getViewDiscId() {
		return viewDiscId;
	}
	public void setViewDiscId(String viewDiscId) {
		this.viewDiscId = viewDiscId;
	}
	public String getUserType() {
		return userType;
	}
	public void setUserType(String userType) {
		this.userType = userType;
	}
	
	
}
