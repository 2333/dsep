package com.dsep.entity.dsepmeta;

public class ProveMaterial {

	private String id;
	private String proveMaterialPath;
	private String materialType;
	private String proveItemId;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getProveMaterialPath() {
		return proveMaterialPath;
	}

	public void setProveMaterialPath(String proveMaterialPath) {
		this.proveMaterialPath = proveMaterialPath;
	}

	public String getMaterialType() {
		return materialType;
	}

	public void setMaterialType(String materialType) {
		this.materialType = materialType;
	}

	public String getProveItemId() {
		return proveItemId;
	}

	public void setProveItemId(String proveItemId) {
		this.proveItemId = proveItemId;
	}

}
